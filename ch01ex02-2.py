# 간단 계산기

num1=int(input('정수를 입력하세요:'))
num2=int(input('정수를 입력하세요:'))

print()
print(f'{num1}과 {num2}을 더하면 {num1+num2}입니다.')
print(f'{num1}을 {num2}로 나누면 {num1/num2}입니다.')
print(f'{num1}나누기 {num2}의 몫은 {num1//num2}입니다.')

